-- Snapshot of game-level predictions for /mlb/predictions, written by a
-- cron once per slate before first pitch. Decouples calibration (was the
-- model right?) from the live-compute page (what does it say right now?).
--
-- Why snapshot at all: the page recomputes on each pageview, so the
-- numbers shift through the day as standings update and probable pitchers
-- get reshuffled. Without a snapshot, we can never answer "what did the
-- model say at 10am, and how did the game actually finish?" — the calibration
-- loop has no foothold.
--
-- Why one snapshot, not many: lineups don't matter for the WIN/NRFI v1
-- model (it's team standings + SP season ERA, not lineup-dependent). One
-- pre-slate snapshot per day is sufficient for calibration.
--
-- model_version is a free-form string ("v1-2026-06-23") that lets us roll
-- the model forward without losing the historical calibration record. When
-- we change the model formula, bump the version; old rows stay attributable
-- to the model they were generated by.
--
-- inputs jsonb stores the actual numbers that went into each prediction
-- (each team's RS/RA/W/L, each SP's ERA). Lets us re-derive a prediction
-- or post-hoc fit a new model without re-pulling daily_raw.

create table public.daily_predictions (
  sport            text         not null,
  date             date         not null,
  game_pk          int          not null,
  model_version    text         not null,
  away_team_id     int          not null,
  home_team_id     int          not null,
  away_win_pct     numeric(5,4) not null,
  home_win_pct     numeric(5,4) not null,
  nrfi_pct         numeric(5,4) not null,
  inputs           jsonb,
  generated_at     timestamptz  not null default now(),
  primary key (sport, date, game_pk)
);

create index daily_predictions_date on public.daily_predictions (sport, date);

alter table public.daily_predictions enable row level security;
grant select, insert, update on public.daily_predictions to service_role;
